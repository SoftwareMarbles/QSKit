#import "QSMath.h"
#import <tgmath.h>


CGFloat QSCeil(CGFloat f) {

	return ceil(f);
}

CGFloat QSAbs(CGFloat f) {

	return fabs(f);
}


CGFloat QSFloor(CGFloat f) {

	return floor(f);
}

